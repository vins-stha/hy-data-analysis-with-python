def vector_angles(X, Y):

    
    prod = X*Y
    dotProduct = np.sum(prod)
    sqA = np.sqrt(np.sum(np.power(X,2)))
    #print(sqA)
    sqB = np.sqrt(np.sum(np.power(Y,2)))
    #print(sqB)
    Theta = dotProduct/(sqA*sqB)
    angle = np.arccos(Theta)
    
    result = angle*180/np.pi
    
    return np.array(result)



